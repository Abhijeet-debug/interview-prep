package com.array;

public class ArrayExample {

	public static void main(String[] args) {

		int arr[] = new int[10];
		arr[0] = 22;
		System.out.println(arr[0]);

		char chArray[] = new char[11];
		chArray[0] = 'A';

		double dArray[] = new double[12];
		dArray[0] = 10;

		System.out.println(chArray[0]);
		System.out.println(dArray[0]);
	}

}
